package Inheritance;



public class Student {
	String name;
	int id;
	String  dept;
	String  dept1;
	String  dept2;
	public Student(String name, int id, String dept, String dept1, String dept2) {
		
		this.name = name;
		this.id = id;
		this.dept = dept;
		this.dept1 = dept1;
		this.dept2 = dept2;
	}
	
	
}
